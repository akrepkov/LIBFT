#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*ptr;

	ptr = (void *)malloc(nmemb * size);
	if (ptr == 0)
		return (0);
	ft_bzero (ptr, size * nmemb);
	return ((void *)ptr);
}
